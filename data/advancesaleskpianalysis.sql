-- Advanced Query 4: Running Revenue Trend
-- Demonstrates

SELECT

sales_month,

monthly_revenue,

SUM(monthly_revenue)
OVER(
ORDER BY sales_month
) AS cumulative_revenue

FROM (

SELECT

DATE_FORMAT(
order_date,
'%Y-%m'
) AS sales_month,

SUM(
p.price *
o.quantity *
(1-o.discount/100)
) AS monthly_revenue

FROM orders o

JOIN products p
ON o.product_id = p.product_id

GROUP BY sales_month

) monthly_sales;












