-- Advanced Query 1: Top Customers
-- ==================================================

SELECT

c.customer_id,

c.city,

ROUND(
SUM(
p.price *
o.quantity *
(1-o.discount/100)
),
2
) AS revenue

FROM orders o

JOIN customers c
ON o.customer_id = c.customer_id

JOIN products p
ON o.product_id = p.product_id

GROUP BY
c.customer_id,
c.city

ORDER BY revenue DESC

LIMIT 10;




