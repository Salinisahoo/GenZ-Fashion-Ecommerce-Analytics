-- Advanced Query 7: High Value vs Low Value Customers
-- Demonstrates how to categorize customers based on their revenue and count the number of customers in each segment.

SELECT

CASE

WHEN revenue >= 50000
THEN 'High Value'

WHEN revenue >= 25000
THEN 'Medium Value'

ELSE 'Low Value'

END AS customer_segment,

COUNT(*) AS total_customers

FROM (

SELECT

customer_id,

SUM(
p.price *
o.quantity *
(1-o.discount/100)
) AS revenue

FROM orders o

JOIN products p
ON o.product_id = p.product_id

GROUP BY customer_id

) customer_revenue

GROUP BY customer_segment;






-- Advanced Query 8: Monthly Growth %

-- This one looks amazing on resumes.

WITH monthly_sales AS (

SELECT

DATE_FORMAT(
order_date,
'%Y-%m'
) AS sales_month,

SUM(
p.price *
o.quantity *
(1-o.discount/100)
) AS revenue

FROM orders o

JOIN products p
ON o.product_id = p.product_id

GROUP BY sales_month

)

SELECT

sales_month,

revenue,

LAG(revenue)
OVER(
ORDER BY sales_month
) AS previous_month,

ROUND(

(
revenue -
LAG(revenue)
OVER(
ORDER BY sales_month
)
)

/

LAG(revenue)
OVER(
ORDER BY sales_month
)

*100,

2

) AS growth_percentage

FROM monthly_sales;





